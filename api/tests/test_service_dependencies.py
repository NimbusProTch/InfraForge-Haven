class TestRequestedServiceSpec:
    def test_valid_postgres(self):
        spec = RequestedServiceSpec(service_type="postgres")
        assert spec.service_type == "postgres"
        assert spec.tier == "dev"
        assert spec.name is None

    def test_valid_redis_with_name(self):
        spec = RequestedServiceSpec(service_type="redis", name="my-cache", tier="prod")
        assert spec.service_type == "redis"
        assert spec.name == "my-cache"
        assert spec.tier == "prod"

    def test_valid_all_types(self):
        for stype in ("postgres", "mysql", "mongodb", "redis", "rabbitmq"):
            spec = RequestedServiceSpec(service_type=stype)
            assert spec.service_type == stype

    def test_invalid_service_type(self):
        with pytest.raises(Exception):
            RequestedServiceSpec(service_type="oracle")

    def test_invalid_tier(self):
        with pytest.raises(Exception):
            RequestedServiceSpec(service_type="postgres", tier="enterprise")

    def test_invalid_name_pattern(self):
        with pytest.raises(Exception):
            RequestedServiceSpec(service_type="postgres", name="My DB")


