def _make_svc(**kwargs):
    defaults = {
        "id": uuid.uuid4(),
        "tenant_id": uuid.uuid4(),
        "name": "myapp-postgres",
        "service_type": ServiceType.POSTGRES,
        "tier": ServiceTier.DEV,
        "status": ServiceStatus.READY,
        "secret_name": "svc-myapp-postgres",
        "service_namespace": "everest",
        "connection_hint": "postgresql://myapp-postgres@myapp-postgres-rw.everest.svc:5432/myapp",
        "credentials_provisioned": True,
        "error_message": None,
    }
    defaults.update(kwargs)
    return types.SimpleNamespace(**defaults)


def _make_app_ns(**kwargs):
    defaults = {
        "id": uuid.uuid4(),
        "tenant_id": uuid.uuid4(),
        "slug": "my-api",
        "env_from_secrets": None,
        "env_vars": {},
        "pending_services": [{"service_name": "myapp-postgres", "service_type": "postgres"}],
        "image_tag": None,
    }
    defaults.update(kwargs)
    return types.SimpleNamespace(**defaults)


def _make_tenant_ns(**kwargs):
    defaults = {
        "id": uuid.uuid4(),
        "slug": "test-tenant",
        "namespace": "tenant-test-tenant",
    }
    defaults.update(kwargs)
    return types.SimpleNamespace(**defaults)


@pytest.mark.asyncio
async def test_auto_connect_matches_pending_service():
    """When a service becomes READY, matching pending_services entry should be connected."""
    from app.main import _auto_connect_pending_services

    tenant = _make_tenant_ns()
    svc = _make_svc(tenant_id=tenant.id)
    app = _make_app_ns(tenant_id=tenant.id)

    # Mock DB
    mock_db = AsyncMock()
    mock_result = MagicMock()
    mock_result.scalars.return_value.all.return_value = [app]
    mock_db.execute.return_value = mock_result
    mock_db.commit = AsyncMock()

    await _auto_connect_pending_services(mock_db, svc, tenant)

    # App should now have env_from_secrets with the connected service
    assert app.env_from_secrets is not None
    assert len(app.env_from_secrets) == 1
    assert app.env_from_secrets[0]["service_name"] == "myapp-postgres"
    assert app.env_from_secrets[0]["secret_name"] == "svc-myapp-postgres"
    assert app.env_from_secrets[0]["database_url_key"] == "DATABASE_URL"

    # pending_services should be cleared
    assert app.pending_services is None

    # DATABASE_URL should be injected into env_vars
    assert "DATABASE_URL" in app.env_vars
    assert app.env_vars["DATABASE_URL"].startswith("postgresql://")


