class TestRequestedServicesFlow:
    """Test the full flow of creating an app with requested services."""

    def test_application_create_schema_accepts_requested_services(self):
        """ApplicationCreate should accept the requested_services field."""
        data = {
            "name": "backend-api",
            "repo_url": "https://github.com/org/backend",
            "requested_services": [
                {"service_type": "postgres"},
                {"service_type": "redis", "tier": "prod"},
                {"service_type": "rabbitmq", "name": "event-bus"},
            ],
        }
        app = ApplicationCreate(**data)
        assert len(app.requested_services) == 3
        assert app.requested_services[0].name is None
        assert app.requested_services[1].tier == "prod"
        assert app.requested_services[2].name == "event-bus"

    def test_service_name_auto_generation(self):
        """When name is None, router should generate {app-slug}-{type}."""
        spec = RequestedServiceSpec(service_type="postgres")
        # Simulate what the router does:
        app_slug = "backend-api"
        svc_name = spec.name or f"{app_slug}-{spec.service_type}"
        assert svc_name == "backend-api-postgres"

    def test_three_tier_demo_services(self):
        """Demo scenario: 3-tier app with PG + Redis + RabbitMQ."""
        app = ApplicationCreate(
            name="demo-api",
            repo_url="https://github.com/org/demo",
            port=8080,
            app_type="web",
            requested_services=[
                RequestedServiceSpec(service_type="postgres"),
                RequestedServiceSpec(service_type="redis"),
                RequestedServiceSpec(service_type="rabbitmq"),
            ],
        )
        assert len(app.requested_services) == 3
        types = [s.service_type for s in app.requested_services]
        assert "postgres" in types
        assert "redis" in types
        assert "rabbitmq" in types
